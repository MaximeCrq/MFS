    <h3>À quoi sert un quiz ?</h3>
    <p>Un quiz sert à tester les connaissances de manière interactive et ludique, tout en stimulant l'apprentissage. Il permet d'engager les participants, en transformant la révision ou  l'apprentissage en un jeu stimulant. 
        Grâce à sa structure de questions-réponses, il encourage l'exploration et la mémorisation de nouveaux concepts, tout en offrant un feedback immédiat. Les quiz sont adaptés à tous les âges et contextes, que ce soit pour l'éducation scolaire, la formation professionnelle ou même le divertissement. Ils favorisent ainsi une approche active et dynamique de l'apprentissage, rendant l'acquisition de savoirs plus attrayante et accessible.
    </p>
